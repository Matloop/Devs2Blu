public class Endereco {
    String rua;
    String bairro;
    String cidade;
    String numeroCasa;
    public Endereco(String rua, String bairro, String cidade, String numeroCasa) {
        this.rua = rua;
        this.bairro = bairro;
        this.cidade = cidade;
        this.numeroCasa = numeroCasa;
    }
}
